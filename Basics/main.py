# print() is a function that prints a message to the console
print("What's up?")
print("I'm a Python programer!")

# Variables
full_name = "John Doe"
age = 30
# boolean variables need to be capitalized
is_Japanese = True
# f stands for format, which allows us to use variables inside the string
# The same concept as template strings in JavaScript
print(f"My name is {full_name}. I'm {age} years old.")
print(f"Am I Japanese? {is_Japanese}")

if(is_Japanese):
    print("I'm Japanese!")
else:
    print("I'm not Japanese!")

